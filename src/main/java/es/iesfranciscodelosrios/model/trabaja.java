package es.iesfranciscodelosrios.model;


public class trabaja {

	private String id_e;
	private String id_p;
	
	
	
	public trabaja(String id_e, String id_p) {
		super();
		this.id_e = id_e;
		this.id_p = id_p;
	}



	public String getId_e() {
		return id_e;
	}



	public void setId_e(String id_e) {
		this.id_e = id_e;
	}



	public String getId_p() {
		return id_p;
	}



	public void setId_p(String id_p) {
		this.id_p = id_p;
	}
	
	
	
	
}
