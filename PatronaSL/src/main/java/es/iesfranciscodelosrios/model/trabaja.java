package es.iesfranciscodelosrios.model;

public class trabaja {

	private int id_e;
	private int id_p;

	public trabaja() {
		super();

	}

	public trabaja(int id_e, int id_p) {
		super();
		this.id_e = id_e;
		this.id_p = id_p;
	}

	public int getId_e() {
		return id_e;
	}

	public void setId_e(int id_e) {
		this.id_e = id_e;
	}

	public int getId_p() {
		return id_p;
	}

	public void setId_p(int id_p) {
		this.id_p = id_p;
	}

}
